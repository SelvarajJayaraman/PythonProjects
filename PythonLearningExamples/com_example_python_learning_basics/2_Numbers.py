# 1.Int
# 2.Float
# 3.Complex

print("#2 Python Numbers Example")
print("*************************")

a = 10
b = 30

c = a + b
print("Addition of Int Values:")
print(type(c))
print(c)
print("\n")

bf = 25.30
cf = 35.50
ef = bf + cf
print("Addition of Float Values:")
print(type(ef))
print(ef)
print("\n")

ac = 3 + 5j
bc = 4 + 6j
cc = ac + bc
print("Addition of Complex Values:")
print(type(cc))
print(cc)
print("\n")

# Power of 10 values
d = 3e5
print("Power of 10:")
print(type(d))
print(d)
